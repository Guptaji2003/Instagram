const mongoose = require("mongoose");

const commentschema = new mongoose.Schema({
  text: {
    type: String,
    required: true,
  },
  author: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
  },
  post: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "Post",
  },
});

const commentmodel = mongoose.model("Comment", commentschema);

module.exports = commentmodel;
